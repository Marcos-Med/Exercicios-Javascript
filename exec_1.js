const operacoes = (number_1 = 0, number_2 = 1) => {
    console.log(`${number_1} + ${number_2} = ${number_1 + number_2}`);
    console.log(`${number_1} - ${number_2} = ${number_1 - number_2}`);
    console.log(`${number_1} * ${number_2} = ${number_1 * number_2}`);
    console.log(`${number_1} / ${number_2} = ${number_1 / number_2}`);
}

operacoes(2,3);
operacoes(3,3);
operacoes(3,4);